# MATHEMATICAL OPERATOR ON SET 
# 1. union-This method return all elements present in both sets. 
x={10,20,30,40}
y={50,60,70,80}
print(x.union(y))

# 2. intersection()-this method returns common elements present in both 
# x and y. 
x={10,20,30,50}
y={40,10,60,20}
print(x.intersection(y))

# 3. difference()-this method returns the elements present in x but not 
# in y
x={10,20,30,50}
y={40,10,60,20}
print(x.difference(y))