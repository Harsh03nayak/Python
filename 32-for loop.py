'''The Python For Loop is used to repeat a block of statements until there are no
items in the Object may be String, List, Tuple, or any other object.

Basically, a for loop is used to iterate elements one by one from sequences like
string, list, tuple, etc. This loop can be easily understood when compared to the
while loop. While iterating elements from the sequence we can perform operations
on every element.'''
#1. Initialization: We initialize the variable(s) here. Example i=1.
# 2. Items in Sequence / Object: It will check the items in Objects. For example, 
# individual letters in String word. If there are items in sequence (True), then it 
# will execute the statements within it or inside. If no item is in sequence 
# (False), it will exit.
# 3. After completing the current iteration, the controller will traverse to the next 
# item.
# 4. Again it will check the new items in sequence. The statements inside it will 
# be executed as long as the items are in sequence.
# example 
