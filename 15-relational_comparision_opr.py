# the operator which are used to check for some relation like greater than 
# or less than, etc b/w obrands 
x=10
y=20
print(x>y) #is greater than
print(x>=y) #is equal or  greater than
print(x<y)  #is less than
print(x<=y) #is equal or less than
print(x==y) #is equal to
print(x!=y) #is not equal to