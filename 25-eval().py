# EVAL FUNCTION
'''The eval()function takes the expression in the form of a string 
and evaluates it and returns the result.'''
# basic expressions
print(eval('10+20'))
print(eval('10-20'))
print(eval('10*20'))
print(eval('10/20'))
print(eval('10//20'))
print(eval('10%20'))
# using input function in eval
x=eval(input("enter the expression :-"))
print(x)